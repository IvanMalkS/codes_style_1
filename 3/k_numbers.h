#ifndef K_NUMBERS_H_
#define K_NUMBERS_H_

#include <string>

/**
 * @file k_numbers.h
 * @brief Заголовочный файл для работы с K-ичными числами
 */

/**
 * @brief Проверяет наличие более трех подряд идущих нулей в числе
 * @param num Строковое представление числа
 * @return true если не более трех нулей подряд, иначе false
 */
bool NoThreeZeros(std::string& num);

/**
 * @brief Вычисляет количество чисел, удовлетворяющих условиям задачи
 * @param k Основание системы счисления (2-10)
 * @param n Количество разрядов (1 < n < 20)
 * @return Количество подходящих чисел
 */
int CalculateCount(int k, int n);

#endif  // K_NUMBERS_H_